#include<stdio.h>

int main() {
    int n, m, k;
    scanf("%d", &n);
    printf("n = %d\n", n);
    for (int i = 1; i <= n; i++) {
        scanf("%d", &m);
        printf("%d ", m);
    }
    printf("\n");
    scanf("%d", &k);
    printf("m = %d\n", k);
    return 0;
}
